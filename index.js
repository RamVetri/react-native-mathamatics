export function add (num1 , num2) {
    const result = num1 + num2
    return result
}

export function sub (num1 , num2) {
    const result = num1 - num2
    return result
}

export function mul (num1 , num2) {
    const result = num1 * num2
    return result
}

export function div (num1 , num2) {
    const result = num1 / num2
    return result
}

export function modulus (num1 , num2) {
    const result = num1 % num2
    return result
}
